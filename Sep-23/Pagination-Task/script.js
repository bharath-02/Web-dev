//  Function which invokes the table
function addData(start,end) {
    // When any pagination links are clicked initially the previous data gets cleared
    document.getElementById('headresult').innerHTML='';
    document.getElementById('bodyresult').innerHTML='';

    //  Creating the Heading of the table
    var row1=document.createElement('tr');
    var head1=document.createElement('th');
    head1.innerHTML='id';
    var head2=document.createElement('th');
    head2.innerHTML='name';
    var head3=document.createElement('th');
    head3.innerHTML='email';

    //  Appending the heading stuffs
    thead.append(row1);
    row1.append(head1,head2,head3);

    //  Looping to create the rows for displaying the data
    for(var itr=start;itr<=end;itr++) {
        //  Creating the rows for the table
        var row=document.createElement('tr');
        var data1=document.createElement('td');
        data1.innerHTML=data[itr].id;
        var data2=document.createElement('td');
        data2.innerHTML=data[itr].name;
        var data3=document.createElement('td');
        data3.innerHTML=data[itr].email;
       
        //  Appending the row stuffs
        tbody.append(row);
        row.append(data1,data2,data3);
    }
}

